       <p>MDIA has embarked on a remarkable journey over the past few years, driven by the performance of Free-to-Air (FTA) TV subsidiary ANTV. From its position as the seventh ranked FTA TV station in 2013, we began to transform with a programming strategy that targeted female viewers and since then, ANTV has consistently focused on family, children and entertainment content, which has proven to be successful in achieving new milestones each year. We give thanks and praise to God for these achievements, especially amidst the considerable political and economic volatility.</p>
	   
<p>MDIA recorded its highest ever revenue in 2016, outperforming the industry with double digit growth of 26.7% to IDR 1,756.6 billion. Operating Income increased by 38.9% to IDR 536.7 billion, with net income amounting to IDR 645.6 billion, representing an increase of 151.5% compared with 2015. These robust results were achieved amidst a volatile political and economic climate, with the Indonesian economy growing 5.0% or or increasing by just 0.2% from 2015 to 2016 and the prospects of Level 1 and Level 2 Regional Head elections in early 2017 as well as tax amnesty issues creating an atmosphere of uncertainty. Amidst these conditions, ANTV continued to deliver exciting and engaging larger-than-life programs that successfully drew the target female audience. The combination of global programs together with rising proportions of domestic content is designed to appeal to audiences and sustain ANTV’s reputation as a trendsetter in the industry they continued to deliver. </p>

<p>ANTV continued to broadcast local series such supplemented by Indian series which remained popular with viewers. Pesbukers was rated as the number one comedy program among entertainment TV stations1. Children’s programs also made a comeback at year end. With regard to marketing, ANTV continued its previous strategy of boosting engagement by encouraging its stars to directly interact with fans over social media to increase viewer engagement. In parallel, ANTV continuously invested in developing its human resources and upgrading its broadcasting infrastructure and equipment such as studio and cameras, as well as its Master Control Room. Such investments are important in order to keep up with the industry and deliver to the standard that viewers expect from a Tier 1 FTA TV station. In particular, we realize the need for fresh talent to support continuous innovation in an industry that thrives on novelty.</p>

<p>ANTV expects to see sustainable growth in 2017, supported by its ability in identifying engaging programs as well as its effective programming strategy, thus strengthening the Company’s brand as well as viewer interest. At the same time, having successfully established ANTV as a Tier 1 FTA brand, MDIA has taken steps to diversify its business lines in order to ensure growth in the future. Complementing ANTV’s FTA TV business, MDIA is establishing a talent agency which is aimed to support ANTV’s growth. In addition, MDIA is also continuously focusing on strengthening its in-house program production capability. These three lines of business will support one another for mutual synergies, and it is expected that they will ultimately generate income streams.</p>

<p>ANTV and MDIA continued to emphasize implementation of good corporate governance principles, including compliance with regulations. On October 14, 2016, ANTV successfully received an extension of its FTA television Operational Broadcasting License (IPP) for the next 10 years from the Ministry of Communications and Informatics of the Republic of Indonesia based on Decree No. 1817 year 2016, demonstrating that ANTV is a station with a good record of compliance with regulations. We will continue to emphasize compliance with regulations in partnership with the government as a good corporate citizen. In addition, MDIA through ANTV also carried out social activities and programs designed to benefit the underprivileged and needy, and bring ANTV closer to its target community.</p>

<p>It has been a stellar year for MDIA and ANTV and I am proud of all our people for their efforts to make this happen. I also wish to express my gratitude to the Board of Commissioners, our loyal viewers, our trusted partners and our valued shareholders for being part of our journey in bringing color to everyday life. I am optimistic that we will be able to sustain and even improve on these achievements, for the benefit of all stakeholders.</p>
